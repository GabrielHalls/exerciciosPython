# Exercicios calcular a area de um triangulo

print("Vamos saber as dimensões de um triangulo \n")

base = float(input("Digite o valor da base do triangulo: "))
altura = float (input("Digite o valor da altura do triangulo: "))

resultado = (base*altura)/2

print("O resultado da area do triangulo é :", resultado)