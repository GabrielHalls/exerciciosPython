# Exercicios de encontrar a area de um trapézio

print("Vamos encontrar a área de um trapézio ? \n")

baseMaior = float(input("Digite o valor da (Base Maior):  "))
baseMenor = float(input("Digite o valor da (Base Menor):  "))
altura = float(input("Digite o valor da altura do trapézio:  "))

resultado = (baseMaior+baseMenor)*altura/2

print("O resultado final com base nos valores fornecidos é : ", resultado)